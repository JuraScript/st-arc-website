document.addEventListener('DOMContentLoaded', () => {
  const langSelector = document.getElementById('langSelector');
  const langActive = document.getElementById('langActive');
  const langDropdown = document.getElementById('langDropdown');
  if (!langSelector || !langActive || !langDropdown) return;

  // Retrieve saved language or default to HR
  let currentLang = localStorage.getItem('starc_lang') || 'hr';

  langActive.textContent = currentLang.toUpperCase();
  updateRTL(currentLang);
  updateLanguage(currentLang, false);

  // Toggle dropdown
  langActive.addEventListener('click', (e) => {
    e.stopPropagation();
    langSelector.classList.toggle('open');
  });

  // Close on outside click
  document.addEventListener('click', (e) => {
    if (!langSelector.contains(e.target)) {
      langSelector.classList.remove('open');
    }
  });

  // Select language
  langDropdown.querySelectorAll('li').forEach(li => {
    li.addEventListener('click', (e) => {
      const selectedLang = e.target.getAttribute('data-lang');
      if (selectedLang !== currentLang) {
        currentLang = selectedLang;
        localStorage.setItem('starc_lang', currentLang);
        langActive.textContent = currentLang.toUpperCase();
        updateRTL(currentLang);
        updateLanguage(currentLang, true);
      }
      langSelector.classList.remove('open');
    });
  });

  function updateRTL(lang) {
    if (lang === 'ar') {
      document.documentElement.setAttribute('dir', 'rtl');
      document.documentElement.setAttribute('lang', 'ar');
    } else {
      document.documentElement.setAttribute('dir', 'ltr');
      document.documentElement.setAttribute('lang', lang);
    }
  }

  function updateLanguage(lang, fade) {
    const elements = document.querySelectorAll('[data-i18n]');
    const enDict = translations['en'];
    const currentDict = translations[lang] || enDict;

    const getTranslation = (key) => {
      if (currentDict && currentDict[key]) return currentDict[key];
      return enDict[key] || '';
    };

    if (fade) {
      elements.forEach(el => el.classList.add('lang-fade-out'));

      setTimeout(() => {
        elements.forEach(el => {
          const key = el.getAttribute('data-i18n');
          const val = getTranslation(key);
          if (val) el.innerHTML = val;

          el.classList.remove('lang-fade-out');
          el.classList.add('lang-fade-in');
        });

        setTimeout(() => {
          elements.forEach(el => el.classList.remove('lang-fade-in'));
        }, 200);
      }, 150);
    } else {
      elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        const val = getTranslation(key);
        if (val) el.innerHTML = val;
      });
    }
  }
});
