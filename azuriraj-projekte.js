const fs = require('fs');
const path = require('path');

const projektiDir = path.join(__dirname, 'Projekti');
const outputFilePath = path.join(__dirname, 'js', 'projects-data.js');

// Helper to generate a slug
function toSlug(str) {
  return str.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/(^-|-$)+/g, '');
}

// Check if a file is an image
function isImage(filename) {
  return /\.(jpg|jpeg|png|webp)$/i.test(filename);
}

// Basic logic to pick the best cover image automatically
function pickCover(images) {
  if (images.length === 0) return '';
  
  // Rule 1: Custom cover file
  let cover = images.find(img => img.toLowerCase().includes('cover'));
  if (cover) return cover;

  // Rule 2: Prefer HDR or premium sounding original files, avoid generic scaled/compressed ones
  const premium = images.find(img => 
    img.includes('HDR') && !img.includes('scaled') && !img.match(/[0-9]+x[0-9]+/)
  );
  if (premium) return premium;

  // Rule 3: Any non-scaled, non-compressed full image
  const normal = images.find(img => !img.includes('scaled') && !img.match(/[0-9]+x[0-9]+/));
  if (normal) return normal;

  // Fallback: first available
  return images[0];
}

function buildConfig() {
  if (!fs.existsSync(projektiDir)) {
    console.error(`Greska: Folder '${projektiDir}' ne postoji!`);
    return;
  }

  const albums = [];
  const folders = fs.readdirSync(projektiDir).filter(f => {
    return fs.statSync(path.join(projektiDir, f)).isDirectory();
  });

  folders.forEach((folder, index) => {
    const folderPath = path.join(projektiDir, folder);
    const files = fs.readdirSync(folderPath).filter(isImage);
    
    // Sort logically
    files.sort();

    const coverImage = pickCover(files);

    albums.push({
      id: index + 1,
      title: folder,
      slug: toSlug(folder),
      subtitle: 'Kolekcija fotografija iz projekta. Uredivo manualno u projects-data.js.',
      cover: coverImage ? `Projekti/${folder}/${coverImage}` : '',
      images: files.map(file => `Projekti/${folder}/${file}`),
      order: index
    });
  });

  // Sort by order 
  albums.sort((a, b) => a.order - b.order);

  const jsContent = `/* 
   GENERATED BY azuriraj-projekte.js
   ------------------------------------
   Ukoliko zelis manualno mijenjati naslove, dodavati opise, 
   ili mijenjati 'cover' sliku, napravi to slobodno ovdje!
   Samo pazi da ne pregazis ovu datoteku ponovnim pokretanjem skripte
   bez arhiviranja tvojih promjena. 
*/\n\nconst PROJECT_ALBUMS = ${JSON.stringify(albums, null, 2)};\n`;

  fs.writeFileSync(outputFilePath, jsContent, 'utf-8');
  console.log(`Uspješno generirano ${albums.length} albuma u ${outputFilePath}`);
}

buildConfig();
